    public static void llenarTablaEmp() throws Exception{
       jtempleados.removeAll(); 
       empleadosDAO control=new empleadosDAO();
       ResultSet res= control.obtenprecios();
           String[] datosTabla={"Id", "Nombre", "Costo","PAQUETE","Comision x sesion"};
           DefaultTableModel datos = new DefaultTableModel(null, datosTabla);
           while(res.next()){                             
                datos.addRow(new Object[]{res.getString("Id"),res.getString("nombre"),res.getString("Costo"),res.getString("paquete"),res.getString("comision")});
           }
           res.close();           
          jtempleados.setModel(datos);           
          jtempleados.getTableHeader().setReorderingAllowed(false);          
          sorter = new TableRowSorter<TableModel>(datos);
          jtempleados.setRowSorter(sorter);
          jtempleados.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
         
          jtempleados.getColumnModel().getColumn(0).setMaxWidth(70);
          jtempleados.getColumnModel().getColumn(2).setPreferredWidth(120);
          jtempleados.getColumnModel().getColumn(2).setMaxWidth(120);                     
    }
    
    
    private void filtrar() {
         RowFilter<TableModel, Object> rf = null;
        try {
            rf = RowFilter.regexFilter("(?i)"+txtbusca.getText(),1,2,3,4);
        }catch (java.util.regex.PatternSyntaxException e) {
        }
        sorter.setRowFilter(rf);
        
    }

