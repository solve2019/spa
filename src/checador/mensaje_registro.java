public void cerrar(){
 Timer timer = new Timer(); 
 // Dentro de 0 milisegundos avÃ­same cada 1000 milisegundos 
 timer.scheduleAtFixedRate(timerTask, 0, 1500);//se epera 3 segundos para cerrar

}

TimerTask timerTask = new TimerTask() 
	   { 
	       public void run()  
	       { 
                  System.out.println("salida");  
	          if(salidas>=1){
                      System.out.println("salida ok"); 
                    salidas=0;
                    checador.start();
                    timerTask.cancel();
                    
                    dispose();
                  }
                  salidas++; 
	       } 
       };
