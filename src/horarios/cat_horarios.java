    public static void llenarTablaHorario() throws Exception{
       jthorarios.removeAll(); 
       horariosDAO control=new horariosDAO();
       ResultSet res= control.obtenerHorarios();
           String[] datosTabla={"Id","Nombre", "Hora Ini", "Hora Fin", "Tolerancia"};
           DefaultTableModel datos = new DefaultTableModel(null, datosTabla);
           while(res.next()){               
                datos.addRow(new Object[]{res.getString("id_horario"),res.getString("nombre"),res.getString("hora_ini"),res.getString("hora_fin"),res.getString("tiempo_tolerancia")});
           }
           res.close();
            jthorarios.setModel(datos);
           
           jthorarios.getTableHeader().setReorderingAllowed(false);          
          sorter = new TableRowSorter<TableModel>(datos);
          jthorarios.setRowSorter(sorter);
          jthorarios.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
          jthorarios.getColumnModel().getColumn(0).setMaxWidth(20);
          
          
           
    }
    
    
    
