    public static void llenarcombo() throws Exception{
       empleadosDAO control=new empleadosDAO();
       ResultSet res= control.obtenempleados();      
           while(res.next()){               
                jcempleado.addItem(res.getString("id_empleado")+"-"+res.getString("nombre")+" "+res.getString("apaterno")+" "+res.getString("amaterno"));
           }
           res.close();
           
    }

    
