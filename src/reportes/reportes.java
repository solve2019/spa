            parametro.put("fecha_ini",fechaini); 
            parametro.put("fecha_fin",fechafin); 
            parametro.put("SUBREPORT_DIR",direc+"/reportes/"); 
            parametro.put("id_emple",id_emp); 
            
            
            //Reporte diseñado y compilado con iReport
