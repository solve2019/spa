    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nota=txtnota.getText().trim();
        String hora=jchora.getSelectedItem().toString();
        String duracion=jcduracion.getSelectedItem().toString();        
        int dura=Integer.parseInt(obtenminutos(duracion));  
        String clien=jccliente.getSelectedItem().toString();
        String user=jcuser.getSelectedItem().toString();
        if(clien.equals("Selecciona..")){        
            JOptionPane.showMessageDialog(null,"Selecciona un cliente.","Mensaje",JOptionPane.ERROR_MESSAGE);
        }else if(user.equals("Selecciona..")){        
            JOptionPane.showMessageDialog(null,"Selecciona un usuario.","Mensaje",JOptionPane.ERROR_MESSAGE);
        }else if(jdfecha.getDate()==null){
            JOptionPane.showMessageDialog(null,"Falta ingresar la fecha.","Mensaje",JOptionPane.ERROR_MESSAGE);
        }else{
            String[] parts = user.split("-");
            String idusers = parts[0];                         
            String[] partcliente = clien.split("-");
            String idcliente = partcliente[0];             
            
            String fechaini=new SimpleDateFormat("yyyy-MM-dd").format(jdfecha.getDate());
            fechaini=fechaini+" "+hora;
            System.out.println(""+fechaini);
            SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd HH:mm");           
            try {
                Date fec=null;
                fec = formatter.parse(fechaini);                               
                Calendar calendar = Calendar.getInstance();
                calendar.setTime(fec); //tuFechaBase es un Date;
                calendar.add(Calendar.MINUTE, dura); //minutosASumar es int.
                Date fechaSalida = calendar.getTime(); //Y ya tienes la fecha sumada.
                String fechafin=new SimpleDateFormat("yyyy-MM-dd HH:mm").format(fechaSalida);
                System.out.println(""+fechafin);
                empleadosDAO control=new empleadosDAO();
            try {
                control.guardarcita(idusers,idcliente,fechaini,fechafin,nota);
                dispose();
            } catch (Exception ex) {
                Logger.getLogger(nueva_cita.class.getName()).log(Level.SEVERE, null, ex);
            }
            } catch (ParseException ex) {
                Logger.getLogger(nueva_cita.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

