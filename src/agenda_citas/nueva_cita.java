 public void llenarTablaEmp() throws Exception{
     
       empleadosDAO control=new empleadosDAO();
       ResultSet res= control.obtenusuarioagenda();     
       while(res.next()){     
            jcuser.addItem(res.getString("idusuario")+"-"+res.getString("nombre"));              
       }
       res.close();                                                           
    }
 
 
 public void llenarTablaclientes() throws Exception{
     
       empleadosDAO control=new empleadosDAO();
       ResultSet res= control.obtenclientes();     
       while(res.next()){     
            jccliente.addItem(res.getString("id_empleado")+"-"+res.getString("cliente"));              
       }
       res.close();                                                           
    }

 











 public String obtenminutos(String tiempo){
     String minutos="0";
     if(tiempo.equals("00:30")){
         minutos="30";
     }
     if(tiempo.equals("01:00")){
         minutos="60";
     }
     if(tiempo.equals("01:30")){
         minutos="90";
     }
     if(tiempo.equals("02:00")){
         minutos="120";
     }
     if(tiempo.equals("02:30")){
         minutos="150";
     }     
     if(tiempo.equals("03:00")){
         minutos="180";
     }
     if(tiempo.equals("03:30")){
         minutos="210";
     }
     if(tiempo.equals("04:00")){
         minutos="240";
     }
     if(tiempo.equals("04:30")){
         minutos="270";
     }
     if(tiempo.equals("05:00")){
         minutos="300";
     }     
     if(tiempo.equals("05:30")){
         minutos="330";
     }
     if(tiempo.equals("06:00")){
         minutos="360";
     }
     return minutos;    
 }
