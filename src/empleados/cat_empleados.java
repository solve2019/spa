    private void jtempleadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtempleadosMouseClicked
        // TODO add your handling code here:
        
       if(evt.getClickCount()==2){
          System.out.println("Se ha hecho doble click");         
          int fila=-1;
           fila=jtempleados.getSelectedRow();

               String id=jtempleados.getValueAt(jtempleados.getSelectedRow(), 0).toString();
               String nombre=jtempleados.getValueAt(jtempleados.getSelectedRow(), 1).toString();
               System.out.println(""+id);


           conexion con=new conexion();
           Statement st1=null;
               try {
                   st1 = con.getConnection().createStatement();
               } catch (SQLException ex) {

               }
           try {                 
               ResultSet rs = st1.executeQuery("SELECT Imagen FROM tc_empleados where id_empleado="+id);
               while (rs.next())
                   {
                           if(rs.getBlob("Imagen")==null){
                               System.out.println("nullo");
                                lbfoto.setIcon(null);
                           }else{
                               Blob blob = rs.getBlob("Imagen");			
                               byte[] data = blob.getBytes(1, (int)blob.length());
                               System.out.println("data:"+data);
                               BufferedImage img = null;
                               try {
                                       img = ImageIO.read(new ByteArrayInputStream(data));
                               } catch (IOException ex) {

                               }
                               ImageIcon imageIcon = new ImageIcon(img);
                               //lbfoto.setIcon(imageIcon);
                               lbfoto.setIcon(new ImageIcon(imageIcon.getImage().getScaledInstance(184,140,Image.SCALE_SMOOTH)));
                           }
                   }
                   rs.close();
                   st1.close();
           } catch (SQLException ex) {
               Logger.getLogger(obtenerfoto.class.getName()).log(Level.SEVERE, null, ex);
           }

          con.desconectar();


          obtenpagos(id,nombre);
       }
    }//GEN-LAST:event_jtempleadosMouseClicked

