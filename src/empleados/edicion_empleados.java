        try{
            con = new conexion();
            st = con.getConnection().createStatement();
            String sentencia="SELECT * from tc_empleados where id_empleado="+id_emple;
            res = st.executeQuery(sentencia);
            while(res.next()){  
               nombre=res.getString("tc_empleados.nombre");
               apaterno=res.getString("apaterno");
               amaterno=res.getString("amaterno");
               clave=res.getString("clave");               
               horaini=res.getString("hora_ini").substring(0,2);
               minini=res.getString("hora_ini").substring(3,5);
               horafin=res.getString("hora_fin").substring(0,2);
               minfin=res.getString("hora_fin").substring(3,5);
               tolerancia=res.getInt("tiempo_tolerancia");
               rmenor=res.getInt("retardo_menor");
               rmayor=res.getInt("retardo_mayor");
               plaza=res.getString("plaza");
               codigo=res.getString("codigo");
               correo=res.getString("correo");
               telefono=res.getString("telefono");
               fechanacio=res.getDate("fecha_nacimiento");
            }
            res.close();
            con.desconectar();
        }catch(Exception ex){
                JOptionPane.showMessageDialog(null,"problemas al obtener la informaci贸n para modificar: "+ex);
        }
        
        initComponents();
        setLocationRelativeTo(null);
        Image icon = Toolkit.getDefaultToolkit().getImage(getClass().getResource("/images/checador.png"));
